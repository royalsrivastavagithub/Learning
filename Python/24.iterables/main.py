#List is iterable
numbers = [1,2,3,4,5]

for num in numbers:
    print(num)

#Tuple is iterable
numbers = (1,2,3,4,5)

for num in numbers:
    print(num)

#String is iterable
numbers = "abcde"

for num in numbers:
    print(num)

#range is iterable
numbers = range(1,10)

for num in numbers:
    print(num)

#Dictionary is iterable
numbers = {"a":1,"b":2,"c":3}

for num in numbers:
    print(num)

#Set is iterable
numbers = {1,2,3,4,5}

for num in numbers:
    print(num)
